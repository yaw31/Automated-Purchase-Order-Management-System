    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String orderId = (String) jComboBox1.getSelectedItem();
        if (orderId == null) {
            JOptionPane.showMessageDialog(this, "Please select an Order ID to delete.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Delete this order?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        boolean deleted = FileHandler.deleteOrder("purchase_order.txt", orderId);
        if (deleted) {
            JOptionPane.showMessageDialog(this, "Order deleted!");
            refreshComboBox();
            ((javax.swing.table.DefaultTableModel) jTable2.getModel()).setRowCount(0);
        } else {
            JOptionPane.showMessageDialog(this, "Order not found or error occurred!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed



