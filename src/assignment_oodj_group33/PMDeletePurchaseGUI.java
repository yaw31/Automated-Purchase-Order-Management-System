    private void refreshComboBox() {
        jComboBox1.removeAllItems();
        List<String> ids = FileHandler.loadOrderIds("purchase_order.txt");
        for (String id : ids) jComboBox1.addItem(id);
    }

    private void showOrderDetail() {
        String orderId = (String) jComboBox1.getSelectedItem();
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        if (orderId != null) {
            String[] data = FileHandler.findOrderById("purchase_order.txt", orderId);
            if (data != null) {
                model.addRow(new Object[]{data[3], data[6], data[7], data[10]});
            }
        }
    }

